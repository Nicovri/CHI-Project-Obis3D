package model.animal;

public class Specie {
	private String scientificName;
	private String order;
	private String superClass;
	
	public String getScientificName() 
	{
		return scientificName;
	}
	
	public String getOrder() 
	{
		return order;
	}
	
	public String getSuperClass() 
	{
		return superClass;
	}
}
